	                                    <hr/><p align="center"><big>1 Основные понятия и принципы использования таблиц CSS</big></p><hr/>

<p style="text-indent:20px">CSS обычно используются в HTML-документах и широко поддержи-ваются различными браузерами. С помощью таблиц стилей можно опреде-лить форматирование индивидуальных элементов, создать классы стилей, установить шрифты, цвета, размещать элементы на странице и задавать дру-гие правила отображение исходной информации в окне. XSL чаще исполь-зуются при работе с XML. XSL – документы формируются в соответствии с правилами создания XML –документов. Таблица CSS определяет только формат и размещение элементов, а XSL может переупорядочивать элементы, полностью менять их, скрывать некоторые из них, выбирать стили основы-ваясь как на этих элементах, так и на их атрибутах.

<p style="text-indent:20px">В связи с использованием в образовательном процессе принципа «от простого к сложному», в данных методических указаниях разберем основы применения таблиц CSS. Именно этот объект обеспечивает отображение до-кументов в виде, изначально задуманном пользователем, в большинстве web-приложений. Существует два уровня таблиц CSS, причем для каждого име-ется своя спецификация, рекомендованная к использованию консорциумом W3C. Уровень CSS2 включает все элементы, относящиеся к уровню CSS1, а также некоторые дополнительные свойства (таблицы стилей для звука, для различных медиа-данных и т.д.). Уровень поддержки CSS различными браузерами различен. В Microsoft Internet Explorer, этот уровень, за исклю-чением нюансов, достаточен для практически одинаковой работы. Хотя при начале работы по разработке web-приложения желательно проверить кор-ректность интерпритации таблиц стилей Вашим браузером.

<p style="text-indent:20px">Таблица CSS представляет собой набор правил стилей, связанных с документом и определяющим форму его отображения web-браузером. При разработке таблицы стилей для каждого элемента документа может быть за-дано соответствующее правило. Правило состоит из селектора, определяю-щего элемент, на который будет направлено действие правила, и определе-ния, заключенного в фигурные скобки, характеризующего особенность отображения документа. Определение в свою очередь состоит из параметра и его значения, отделенных двоеточием.<br> <br>

