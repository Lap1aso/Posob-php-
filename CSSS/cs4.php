			                                  <hr/><p align="center"><big>4 СВОЙСТВА, ВЛИЯЮЩИЕ НА ФОРМАТИРОВАНИЕ ЭЛЕМЕНТОВ</big></p><hr/>

<p style="text-indent:20px">Все свойства форматирования HTML-документов, доступные в каскад-ных таблицах стилей первого и второго уровня, можно разделить на 9 кате-горий:

<p style="text-indent:10px">- шрифт: различные типографские свойства;
<p style="text-indent:10px">- текст: выравнивание, межсимвольное расстояние и форматирование;
<p style="text-indent:10px">- цвет: цвет текста;
<p style="text-indent:10px">- фон: цвет фона и фоновые изображения;
<p style="text-indent:10px">- блок: свойства форматирования отдельных блоков;
<p style="text-indent:10px">- визуальное форматирование: свойства, создающие блоки из отобра-жаемых элементов и позиционирующие их, а также вывод списков;
<p style="text-indent:10px">- переходы и фильтры: определение мультимедийных эффектов, преоб-разование изображений;
<p style="text-indent:10px">- печать: свойства разметки страниц;
<p style="text-indent:10px">- псевдоклассы и другие свойства: свойства псевдокласса связей, свой-ства cursor, impotant и @import.

<p style="text-indent:20px">Для задания значений свойств таблиц стилей, определяющих размеры элементов, используются относительные и абсолютные единицы. Относи-тельные единицы задают размер элемента относительно ранее заданной ве-личины. Документы, использующие относительные единицы измерения, лучше приспособлены для отображения на различных устройствах. Абсо-лютные единицы желательно использовать в случаях, когда известны метри-ческие характеристики устройства отображения.

<p style="text-indent:20px">Основные единицы измерения, используемые в web-дизайне, предста-вим в табличном виде.

<p style="text-indent:20px">Пикселы соотносятся с разрешением монитора персонального компью-тера. Если плотность пикселов на единицу площади на устройстве вывода значительно отличается от значения для монитора, браузер переопределяет эту единицу. Таким образом, появляется новая единица, ссылочный или условный пиксел, которым является угол, под которым виден один пиксел монитора с плотностью 90 dpi с расстояния вытянутой руки (примерно 28 дюймов).<br> <br>

