                                                         <hr/> <p align="center"><big>1 Структура HTML-документа</big></p><hr/>

 <p style="text-indent:20px">Самым главным из тегов HTML является одноименный тег -- <.HTML>. Он должен всегда открывать ваш документ, так же, как тег <./HTML> должен непременно стоять в последней его строке. Эти теги обозначают, что находящиеся между ними строки представляют единый гипертекстовый документ. Без этих тегов браузер или другая программа просмотра не в состоянии идентифицировать формат документа и правильно его интерпретировать. 

<p style="text-indent:20px">Закрывающий тег так же важен, как и открывающий. Если, например, документ включен в электронное письмо, тег <./HTML> дает команду программе просмотра прекратить интерпретацию текста, как HTML-кода. 
HTML-документ состоит из двух частей: заголовок (head) и тело (body), расположенных в следующем порядке:<br> 

<span class="letter"><.HTML><br>
  <.HEAD><br>
       ...<br>
  <./HEAD><br>

  <.BODY><br>
       ...<br>
  <./BODY><br>
<./HTML><br></span>
<p style="text-indent:20px">В HTML-документ можно включать комментарии, позволяющие скрыть часть текста от браузера. Все, что заключено между последовательностями символов <.!-- и -->, при просмотре страницы остается невидимым. Комментарии не могут быть вложенными друг в друга. <hr/>

                                                              <p align="center"><big>2 Заголовок документа</big></p><hr/>

<p style="text-indent:20px">Включение в документ заголовочной части не является обязательным. Задачей заголовка является представление необходимой информации для браузера и сервера HTTP. Информация, размещенная внутри заголовка документа, обычно не выводится на экран (кроме названия документа). 

<p style="text-indent:20px">Заголовок документа открывается тегом <.HЕAD>, который обычно следует сразу же за тегом <.HTML>. Закрывающий тег <./HEAD> показывает конец этого раздела, между ними располагаются остальные теги заголовка документа. 

<p style="text-indent:20px">Чаще всего в заголовок документа включают парный тег <.TITLE> ... <./TITLE>, определяющий название документа. Многие программы просмотра используют его как заголовок окна, в котором выводят документ. Программы, индексирующие документы в сети Интернет, используют название для идентификации страницы. Хорошее название должно быть достаточно длинным для того, чтобы можно было корректно указать соответствующую страницу, и в то же время оно должно помещаться в заголовке окна. Название документа вписывается между открывающим и закрывающим тегами.<hr/>

                                                                  <p align="center"><big>3 Тело документа</big></p><hr/>

<p style="text-indent:20px">В отличии от заголовка, тело документа является обязательным элементом, так как в нем располагается весь материал вашего документа (есть только одно исключение, с которым мы познакомимся далее, -- когда документ содержит вместо тела группу фреймов). Тело документа размещается между тегами <.BODY> и <./BODY>. Все, что размещено между этими тегами, интерпретируется браузером в соответствии с правилами языка HTML, позволяющими корректно отображать страницу на экране монитора. 

<p style="text-indent:20px">Тег <.BODY> не только обозначает начало содержимого документа, но и задает его основные свойства: цвет фона, текста и многое другое. Эти свойства определяются с помощью атрибутов, которые приведены в таблице.<br>

