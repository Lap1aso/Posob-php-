                                                    <hr/><p align="center"><big>1 Использование графики в HTML-документах</big></p><hr/>

<p style="text-indent:20px">Рисунки и анимация могут сделать HTML-документ более привлекательным и интересным. Они не только украшают страницу, но и помогают лучше передать содержание документа. Для правильного использования графики в HTML-документе необходимо учитывать следующие факторы: многие браузеры поддерживают только графические форматы GIF и JPEG; файлы, содержащие графику, передаются медленно; некоторые пользователи не имеют графических браузеров или намерено отключают загрузку изображений; цветная графика, которая хорошо смотрится на вашем компьютере, может плохо выглядеть на другом. 

<p style="text-indent:20px">Тег <.IMG> вставляет изображение в документ, как если бы оно было просто одним большим символом. Синтаксис тега: 

<p style="text-indent:40px"><.IMG атрибут1=значение1 ... [атрибутN=значениеN]> 

<p style="text-indent:20px">Атрибуты тега <.IMG> и их значения приведены в таблице. <br><br>

	<table border="1">
<tr>
<th>Атрибут</th>
<th>Назначение</th>
</tr>
<tr>
<td> SRC="файл"  </td>
<td>Задает URL-адрес изображения (можно указывать как абсолютный, так и относительный URL-адрес; если файл с изображением находится в той же директории, что и HTML-документ, то достаточно просто указать имя файла); этот атрибут является обязательным </td>
</tr>
<tr>
<td> ALT="текст" </td>
<td>Задает альтернативный текст для браузеров, не поддерживающих работу с изображениями </td>
</tr>
<tr>
<td> ALIGN="тип" </td>
<td> Задает расположение картинки относительно текста, тип может принимать следующие значения: ТОР, MIDDLE, BOTTON, LEFT, RIGHT  </td>
</tr>
<tr>
<td> BORDER=n</td>
<td> Устанавливает толщину обрамления вокруг изображения в пикселах </td>
</tr>
<tr>
<td> HEIGHT=n(%)</td>
<td> Устанавливает высоту изображения в пикселах или в процентах от высоты окна браузера </td>
</tr>
<tr>
<td> WIDTH=n(%)</td>
<td> Устанавливает ширину изображения в пикселах или в процентах</td>
</tr>
<tr>
<td> HSPACE=n</td>
<td> Задает свободное пространство слева и справа от изображения (в пикселах) </td>
</tr>
<tr>
<td> VSPACE=n</td>
<td> Задается свободное пространство над и под изображением (в пикселах)</td>
</tr>
</table> <br><br>

<p style="text-indent:20px">Обратите внимание, что ширина и высота изображения могут быть заданы не только в пикселах, но и в процентах от размеров окна браузера. Многие компоненты, включаемые в состав Web-страниц (изображения, таблицы, аплеты и т. д.), позволяют задавать размер в относительных единицах (т. е. в процентах). Это позволяет уменьшить зависимость внешнего вида документа от текущих установок конкретного браузера и особенностей операционной системы. Рекомендуется задавать только один из атрибутов пары "ширина-высота" изображения, иначе рисунок может быть непропорционально деформирован и изменит свой вид. <br><br>

Пример<br><br>

<p style="text-indent:20px">Eсли размер изображения, хранящегося в файле exm2.gif составляет 150 пикселов по ширине и 90 по высоте, то следующая команда приведет к включению в документ деформированного изображения: <.IMG SRC="exm2.gif" WIDTH=50 HEIGHT=200> . 

<p style="text-indent:20px">Помните, что графика передается по сети намного медленнее, чем текст, поэтому лучше включать в документ картинки небольшого размера. Если вы задаете размер изображения меньше, чем у исходного, то это приводит только к неоправданному увеличению объема передачи информации по сети. Рациональнее предварительно уменьшить размер изображения с помощью графического редактора. 

<p style="text-indent:20px">Язык HTML позволяет задать расположение изображения относительно окружающего его текста. Атрибут ALIGN может принимать следующие значения. <br><br>

		<table border="1">
<tr>
<th>Значение</th>
<th>Функция</th>
</tr>
<tr>
<td> ТОР  </td>
<td> Выравнивает одну строку по верху изображения, остальные помещает после рисунка </td>
</tr>
<tr>
<td> MIDDLE  </td>
<td> Выравнивает одну строку по середине изображения, остальные помещает после рисунка  </td>
</tr>
<tr>
<td>BOTTOM</td>
<td> Выравнивает одну строку по низу изображения, остальные помещает после рисунка </td>
</tr>
<tr>
<td> LEFT  </td>
<td> Прижимает обтекаемое текстом изображение к левой стороне окна браузера  </td>
</tr>
<tr>
<td> RIGHT   </td>
<td> Прижимает обтекаемое текстом изображение к правой стороне окна браузера  </td>
</tr>
</table> <br><br>

Пример<br>
