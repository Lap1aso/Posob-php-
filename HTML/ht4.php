                                                   <p align="center"><big>3 Абсолютный и относительный URL</big></p><hr/>

<p style="text-indent:20px">В разделе, посвященном компьютерным сетям и принципам поиска информации в Интернет, было введено понятие URL (универсального локатора ресурса), однозначно определяющего расположение объекта в глобальной сети. В общем случае URL имеет следующий вид (части, заключенные в квадратные скобки, не обязательны и могут быть опущены): 

<p style="text-indent:40px">протокол://адрес_узла[:порт]/путь/файл[#метка]<br><br>

В таблице ниже приведены назначения компонент URL.<br><br> 

<table border="1">
<tr>
<th>Компонента</th>
<th>Назначение</th>
</tr>
<tr>
<td> протокол </td>
<td> Обозначение одного из протоколов, используемых для обращения к ресурсу, возможные значения: http, ftp, file и др. </td>
</tr>
<tr>
<td> адрес_узла </td>
<td> Доменное имя или IP-адрес компьютера в сети Интернет  </td>
</tr>
<tr>
<td> порт </td>
<td> Порт, по которому клиент обращается к серверу для установления соединения; указывается только в случае обращения к нестандартному порту </td>
</tr>
<tr>
<td> путь </td>
<td> Путь к требуемому ресурсу  </td>
</tr>
<tr>
<td> файл </td>
<td> Имя файла, содержащего HTML-документ или другой ресурс </td>
</tr>
<tr>
<td> метка</td>
<td> Позиция в документе, начиная с которой он отображается в окне браузера  </td>
</tr>
</table> <br><br>

<p style="text-indent:20px">URL, заданный в таком виде, называется абсолютным URL, так как он полностью описывает расположение ресурса в глобальной сети. 

Пример <br><br>

<p style="text-indent:20px">При обращении к конкретному ресурсу порт и позиция в документе часто не указываются: 

<p style="text-indent:20px">http://www.ctc.msiu.ru/education/book/index1.html<br><br>

Пример<br><br>

<p style="text-indent:20px">Рассмотрим ссылку на так называемую домашнюю страницу (Home Page) конкретного пользователя. Предположим, что на сервере www.msiu.ru зарегистрирован человек с пользовательским именем (login) ivanov. Домашней страницей называют файл index.html, находящийся в директории с именем public_html, которая, в свою очередь, должна располагаться в домашней директории пользователя. Тогда ссылка на домашнюю страницу этого человека может быть задана в виде 

<p style="text-indent:20px"><span class="letter"><.A HREF="http://www.msiu.ru/~ivanov">Текст ссылки</span></A>

<p style="text-indent:20px">Обратите внимание, что имя самого файла при такой записи не указывается. Если потребуется сослаться на какой-либо другой документ данного пользователя, расположенный в директории public_html, например, photo.html, то ссылка примет вид: 

<p style="text-indent:20px"><span class="letter"><.A HREF="http://www.msiu.ru/~ivanov/photo.html">Текст</span></A>

<p style="text-indent:20px">Относительный URL описывает положение ресурса, на который указывает ссылка, относительно URL текущего документа. 

<p style="text-indent:20px">Пример

<p style="text-indent:20px">При задании относительных ссылок указывается путь по файловому дереву до того места, где находится требуемый HTML-ресурс. Вот ссылка на документ image.html, размещенный в текущем каталоге: 

<p style="text-indent:20px"><span class="letter"><.A HREF="image.html">Ссылка</span> </A>.

<p style="text-indent:20px">Если файл pict.html лежит в родительском по отношению к текущему документу каталоге, то следует использовать запись 

<p style="text-indent:20px"><span class="letter"><.A HREF="../pict.html">Текст ссылки</span></a>

<p style="text-indent:20px">Если же требуемый документ находится в поддиректории Picture, то используется запись, аналогичная следующей 

<p style="text-indent:20px"><span class="letter"><.A HREF="Picture/pict.html">Текст ссылки</span></A><hr/>

	                                <p align="center"><big>4 Гиперссылки в пределах одного документа</big></p><hr/>

<p style="text-indent:20px">Другая форма тега <A> предназначена для присваивания имени некоторому фрагменту документа HTML: 

<p style="text-indent:20px"><span class="letter"><.A NAME="имя">фрагмент документа</span></A>

<p style="text-indent:20px">Тег <A> часто называют тегом якоря (anchor). Если якорь применяется для добавления метки (имени) к фрагменту документа, то его называют именованным якорем. 

<p style="text-indent:20px">Для того чтобы сослаться на фрагмент, которому присвоено имя, используется следующая форма тега <.A>: 

<p style="text-indent:20px"><span class="letter"><.A HREF="URL_ресурса#имя">Текст ссылки</span></A>

<p style="text-indent:20px">Если нужно сослаться на фрагмент текущего документа, то URL-ресурса можно опустить: 

<p style="text-indent:20px"><span class="letter"><.A HREF="#имя">Текст ссылки</span></A><br><br>

Пример<br>

<p style="text-indent:20px">Пусть в документе с именем book.html заголовку главы 2 присвоено имя chapter2: 

<p style="text-indent:20px"><span class="letter"><.A NAME="chapter2">Глава 2</span></A>

<p style="text-indent:20px">Тогда ссылка на эту главу, расположенная в этом же документе, будет иметь вид: 

<p style="text-indent:20px"><span class="letter"><.A HREF="#chapter2">Глава 2</span></A>

<p style="text-indent:20px">Для задания ссылки на эту же метку файла book.html из другого файла нужно написать: 

<p style="text-indent:20px"><span class="letter"><.A HREF="book.html#chapter2">Глава 2</span></A><hr/>

                                              <p align="center"><big>5 Ссылка на почтовый ящик </big></p><hr/>

<p style="text-indent:20px">Для того чтобы создать ссылку на почтовый ящик, напишите: 

<p style="text-indent:20px"><span class="letter"><.A HREF=mailto:адрес_e-mail>текст ссылки</span></A>

<p style="text-indent:20px">Здесь вместо адрес_e-mail нужно поставить адрес почтового ящика. После того, как посетитель активизирует эту ссылку, у него на экране появится специальное окно, позволяющее послать письмо по указанному адресу. <br><br>

Пример <br>

