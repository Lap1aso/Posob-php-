                                                         <p align="center"><big>2 Гиперссылки</big></p><hr/>


<p style="text-indent:20px">Несмотря на то, что в состав HTML-документа входят самые различные компоненты, можно сказать, что гипертекстовые ссылки - основа WWW. Если бы Web-страницы не ссылались друг на друга, содержимое Web превратилось бы в обычный набор файлов, не связанных между собой. 

<p style="text-indent:20px">Для создания гипертекстовой ссылки используется пара тегов <A>...</A>. Фрагмент текста, изображение или любой другой объект, расположенный между этими тегами, отображается в окне браузера как гипертекстовая ссылка. Активация такого объекта приводит к загрузке в окно браузера нового 
документа или к отображению другой части текущей Web-страницы. Гипертекстовая ссылка формируется с помощью выражения 

<p style="text-indent:20px"><.A HREF="URL_ресурса">фрагмент документа<./A>
HREF здесь является обязательным атрибутом, значение которого и есть URL-адрес запрашиваемого ресурса. Кавычки в задании значения атрибута HREF не обязательны. 

<p style="text-indent:20px">Tекстовые указатели, т. е. фрагменты текста, являющиеся ссылками, не отличаются разнообразием внешнего вида. Обычно такой указатель представляет собой слово или слова, подчеркнутые прямой линией. Цвет указателя может регулироваться автором и установками программы просмотра. <br>

	Пример<br>

<p style="text-indent:40px"><A HREF="example.html">Ссылка</A> 
<p style="text-indent:40px">Графические указатели, т. е. изображения-ссылки, выделяются рамкой того же цвета, что и текстовые указатели. <br><br>

