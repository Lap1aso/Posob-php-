	                                                 <hr/> <p align="center"><big>1 Списки </big></p><hr/>

<p style="text-indent:20px">В настоящее время стандарты HTML поддерживают теги для списков трех различных видов: нумерованных (упорядоченных), маркированных (неупорядоченных) и списков определений. Списки и элементы списков являются блочными элементами. Это означает, что перед ними и после них автоматически добавляются пустые строки. 

<p style="text-indent:20px">Язык HTML допускает вложенность любых видов списков. Для этого размещают одну пару тегов (стартовый и завершающий) внутри другой. Следует помнить о том, что все имеющиеся списки должны завершаться закрывающим тегом. <hr/>

	                                              <p align="center"><big>2 Нумерованные списки </big></p><hr/>

<p style="text-indent:20px">Нумерованные (упорядоченные) списки используют, когда важен порядок вывода элементов списка. Браузер автоматически вставляет номера элементов по порядку, в исходном HTML-тексте номера не печатаются. Если количество элементов списка изменится (в результате удаления или добавления новых элементов), то нумерация автоматически обновится.

<p style="text-indent:20px">Весь нумерованный список заключается между парой тегов <.OL> и <./OL>, а каждый элемент списка расположен между тегами <.LI> и <./LI> (закрывающий тег </LI> может отсутствовать).<br><br> 

<img border="0" src="primer/prim8.png" alt="Пример" align=right width="400" height="200"><br>

	Пример <br>

<span class="letter"><.OL> <br>
 <.LI>Программирование<./LI> <br>
 <.LI>Алгоритмизация<./LI> <br>
 <.LI>Проектирование<./LI> <br>
<./OL> </span><br><br>

<p style="text-indent:20px">Тег <.OL> может имеет атрибуты TYPE и START: 
<p style="text-indent:20px"><.OL START=n TYPE=вид_счетчика>
<p style="text-indent:20px">Атрибут TYPE задает вид счетчика, возможные значения которого приведены в таблице, a START -- начальное значение. <br><br>

