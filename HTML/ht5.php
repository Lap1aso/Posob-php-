<td> Устанавливает цвет фона всей таблицы</td>
</tr>
<tr>
<td> BACKGROUND=URL </td>
<td> Указывает фоновое изображение для таблицы, где URL -- адрес источника (имя файла с изображением)</td>
</tr>
</table> <br> <br>

