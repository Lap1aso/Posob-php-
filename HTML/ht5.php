                                   <p align="center"><big>8 Выравнивание данных в ячейках </big></p><hr/>

<p style="text-indent:20px">При помощи атрибутов ALIGN и VALIGN можно по-разному размещать данные относительно границ ячейки. Эти атрибуты используются совместно с тегами <.CAPTION>, <.TR>, <.TH> и <.TD> в самых различных комбинациях. Ниже приведены значения атрибутов для перечисленных элементов. <br> <br>

 	<table border="1">
