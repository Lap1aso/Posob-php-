<td>Определяет высоту всей таблицы в пикселах, либо в процентах от высоты окна браузера   </td>
</tr>
<tr>
<td> ALIGN </td>
<td> Задает горизонтальное выравнивание таблицы в окне браузера; имеет значения left, center и right (по умолчанию -- left) </td>
