•	цвет таблицы -- белый; <br>
•	вторая строка светло-серого цвета, в ячейке 2 -- цвет "teal"; <br>
•	цвет надписи в ячейке 5 -- красный; <br>
•	первый столбец составляет 20% от ширины таблицы, два другие -- по 40%; <br>
•	ячейка 3 центрирована, а ячейка 4 -- выровнена вправо; <br>
•	ячейки 6 и 8 объединены в одну, центрированы и выровнены по нижнему краю ячейки; <br>
•	поля внутри ячеек -- 10 пикселей. <br> <br>

