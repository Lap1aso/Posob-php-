                                                                 <p align="center"><big>3  Маркированные списки </big></p><hr/>

<p style="text-indent:20px">Маркированный (неупорядоченный) список используется для представления коллекции элементов, порядок вывода которых не важен. При выводе маркированных списков браузер автоматически вставляет специальные значки (маркеры), отмечающие каждый элемент списка. 
	
<p style="text-indent:20px">Маркированный список начинается стартовым тегом <.UL> и завершается тегом <./UL>. Каждый элемент списка начинается с тега <.LI> и завершается (необязательным) тегом <./LI>. <br><br><br>

<img border="0" src="primer/prim10.png" alt="Пример" align=right width="400" height="200"> 

Пример <br><br>

<span class="letter"><.UL> <br>
 <.LI>Программирование<./LI> <br>
 <.LI>Алгоритмизация<./LI> <br>
 <.LI>Проектирование<./LI>	 <br>
<./UL> 	</span> <br> <br> <br> <br> <br><br>



<p style="text-indent:20px">Тег <.UL> имеет атрибут TYPE, определяющий внешний вид маркера: <.br>

<p style="text-indent:20px"><.UL TYPE=тип_маркера><.br> Значение атрибута TYPE может быть одним из следующих:: disc (круг -- форма по умолчанию), circle (окружность) или square (квадрат). <.br><br><br>

<img border="0" src="primer/prim11.png" alt="Пример" align=right width="400" height="200">

Пример <br><br>

<span class="letter"><.UL TYPE=square> <br>
 <.LI>Программирование<./LI> <br>
 <.LI>Алгоритмизация<./LI> <br>
 <.LI>Проектирование<./LI> <br>
<./ul> </span>	<br> <br> <br> <br> <br> <br>

<p style="text-indent:20px">Атрибут TYPE применяется и в теге <.LI> для изменения формы маркера перед конкретным элементом списка.<br><br>

<img border="0" src="primer/prim12.png" alt="Пример" align=right width="400" height="200">

Пример <br><br>

<span class="letter"><.UL> <br>
 <.LI TYPE=circle><br>
     Программирование<br>
 <.LI TYPE=disk><br>
     Алгоритмизация<br>
 <.LI TYPE=square><br>
     Проектирование<br>
<./ul> 	</span> <br><br><br><br>


<p style="text-indent:20px">Тег <.LI> обеспечивает вывод маркера и разделение элементов списка. Если хочется использовать нестандартные маркеры, то тег <.LI> не указывается. Для выделения элементов списка в этом случае используются какие-либо картинки или символы, а тег <.BR> обеспечивает переход к следующему элементу списка. <br><br>

<img border="0" src="primer/prim13.png" alt="Пример" align=right width="400" height="200">

Пример <br><br>

<span class="letter"><.UL> <br>
 <.IMG SRC=arr.gif ALIGN=top><br>
 <.nbr>Программирование<.BR> <br>
 <.IMG SRC=arr.gif ALIGN=top><br>
 <.nbr>Алгоритмизация<.BR> <br>
 <.IMG SRC=arr.gif ALIGN=top><br>
 <.nbr>Проектирование<.BR> <br>
<./UL> 	</span> <br><br><br><hr/>





                                     <p align="center"><big>4  Список определений  </big></p><hr/>

<p style="text-indent:20px">Список описаний (список определений) начинается с тега <.DL> и завершается тегом <./DL>. Данный список служит для создания списков типа «термин» – «описание». Термин автоматически размещается у левой границы страницы, а их определения смещены относительно них вправо. Каждый термин обозначается тегом <.DT>, а его описание – тегом <.DD>. <br><br>

<img border="0" src="primer/prim14.png" alt="Пример" align=right width="400" height="200">

Пример <br><br>
<span class="letter"><.DL> <br>
 <.DT>Отдел маркетинга <br>
 <.DD>Данный отдел занимается продвижением товаров и услуг <br>
 <.DT>Финансовый отдел <br>
 <.DD>Данный отдел занимается финансовыми операциями <br>
<./DL> </span>    <br> <br> <br> <br>

<p style="text-indent:20px">Отметим, что все вышеперечисленные виды списков имеют атрибут СОМРАСТ, позволяющий выводить списки в более компактном виде. <hr/>

	                                     <p align="center"><big>5  Вложенные списки  </big></p><hr/>


<p style="text-indent:20px">Любой список может быть частью другого списка, вложен в другой список. Считается полезным использование сдвигов при подготовки текста исходного HTML-документа, чтобы четко представлять уровни вложенности списков. <br><br>

<img border="0" src="primer/prim15.png" alt="Пример" align=right width="400" height="250">
 
<span class="letter"><.OL><br>
 <.LI>Дирекция<./LI> <br>
 <.UL> <br>
   <.LI>Иванов И.И.<./LI> <br>
   <.LI>Петров К.В.<./LI> <br>
 <./UL>  <br>
 <.LI>Отдел маркетинга<./LI><br>
 <.UL>  <br>
  <.LI>Варшавская Е.Л.<./LI> <br>
  <.LI>Самсонов Д.М.<./LI> <br>
 <./UL> <br>
<./OL> 	</span>   <br> <br> <br> <br>



<p style="text-indent:20px">При выводе вложенных маркированных списков браузер автоматически проставляет маркеры перед элементами, находящимися на разных уровнях вложенности.

<p style="text-indent:20px">Для вложенных нумерованных списков браузер, к сожалению, не изменяет тип нумерации. По умолчанию каждый уровень внутри такого списка будет отмечен арабскими цифрами. Для получения списка в другом формате потребуется вручную проставить каждую метку, используя атрибут TYPE. <br><br>
 
<img border="0" src="primer/prim16.png" alt="Пример" align=right width="400" height="250">

<span class="letter"><.OL TYPE=I><br>
 <.LI>Дирекция<./LI> <br>
 <.OL> <br>
   <.LI>Иванов И.И.<./LI> <br>
   <.LI>Петров К.В.<./LI> <br>
 <./OL>  <br>
 <.LI>Отдел маркетинга<./LI><br>
