                                <p align="center"><big>9 Объединение ячеек </big></p><hr/>

<p style="text-indent:20px">Смежные ячейки таблицы могут объединяться. Например, в таблице из нескольких столбцов все ячейки первой строки можно объединить и поместить в этой строке красивый заголовок таблицы. Возможно также объединение нескольких строк или создание пустой прямоугольной области. 

<p style="text-indent:20px">Для соединения двух смежных ячеек в одном столбце нужно использовать атрибут ROWSPAN тега <.TH> или <.TD>, например,<br> <br> 

<span class="letter"><.TD ROWSPAN=2></span>

<p style="text-indent:20px">Для объединения двух смежных ячеек в одной строке нужно использовать атрибут COLSPAN тех же тегов, например, <br> <br>

<span class="letter"><.TD COLSPAN=2></span> <br> <br>


Пример<br> <br>
В следующей таблице используется объединение столбцов и строк. <br> <br>

<img border="0" src="primer/prim19.png" alt="Пример" align=right width="500" height="250">

<span class="letter"><.HTML><br>
<.BODY><br>
<.TABLE BORDER=1 ALIGN=center><br>
<.TR><.TH COLSPAN=3>Заголовок на 3 столбца<./TH><./TR> <br>
<.TR><br>
<.TH>Заголовок на 1 строку<./TH><br>
<.TD>Ячейка 1<./TD><br>
<.TD>Ячейка 2<./TD><br>
<./TR><br>
<.TR><br>
<.TH ROWSPAN=3>Заголовок на 3 строки<./TH><br>
<.TD>Ячейка 3<./TD><br>
