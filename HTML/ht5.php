                                      <p align="center"><big>6 Использование таблиц в HTML  </big></p><hr/>

<p style="text-indent:20px">Теги HTML для создания таблиц первоначально предназначались для представления строк и столбцов табулированных данных. Однако дизайнеры научились с их помощью управлять разметкой Web-страниц: создавать столбцы текста, задавать интервалы между элементами и изменять внешний вид текста способами, недоступными другим тегам форматирования HTML. 

<p style="text-indent:20px">Таблицы в языке HTML всегда имеют прямоугольный вид и состоят из строк, которые в свою очередь состоят из ячеек. Все языковые конструкции, описывающие компоненты создаваемой таблицы, заключаются между тегами <.TABLE> и <./TABLE>. 

<p style="text-indent:20px">Заполнение таблицы происходит построчно; для обозначения строки используется пара тегов <.TR>...<./TR>. Строка состоит из ячеек, для задания которых используют либо теги <.TH>...<./TH>, если эти ячейки содержат заголовки столбцов, либо теги <.TD>...<./TD>. Заголовки выводятся полужирным шрифтом и располагаются по центру ячейки. Данные имеют обычный шрифт и выравниваются по левой стороне ячейки. Для задания заголовка всей таблицы используются теги <.CAPTION> и <./CAPTION>. <br> <br>

	<img border="0" src="primer/prim17.png" alt="Пример" align=right width="500" height="250">

	Пример <br> <br>

<span class="letter"><.TABLE> <br>
<.CAPTION>Заголовок таблицы<./CAPTION> <br>
<.TR><.TH>Заголовок 1</.TH><.TH>Заголовок 2<./TH><./TR> <br>
<.TR><.TD>Ячейка 1<./TD><.TD>Ячейка 2<./TD><./TR> <br>
<.TR><.TD>Ячейка 3<./TD><.TD>Ячейка 4<./TD><./TR> <br>
<./TABLE></span> <br> <br> <br> <br> <br> <br> <br> 

<p style="text-indent:20px">Наличие в ячейках данных не обязательно. Создать пустую ячейку можно двумя способами: ничем не заполнять ее контейнер (<.TD></.TD>), либо поместить в нее символ неразрывного пробела, задаваемого специальной последовательностью символов -- &.nbsp; (т. е. создать ячейку вида <.TD>&nbsp;<./TD>). 

<p style="text-indent:20px">Нет надобности отдельно создавать пустые ячейки, если планируется, что все оставшиеся в строке ячейки не будут заполнены. Так как тег <.TR> сигнализирует о начале новой строки, пустые ячейки будут добавлены браузером автоматически.<hr/> 


                                     <p align="center"><big>7 Основные атрибуты тега <.TABLE> </big></p><hr/>

                             
<p style="text-indent:20px">Назначение основных атрибутов тега <.TABLE> и значения, которые они могут принимать перечислены в таблице. <br> <br> 

	<table border="1">
<tr>
<th>Атрибут</th>
<th>Назначение</th>
</tr>
