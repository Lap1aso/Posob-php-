<p style="text-indent:20px">Кадру обязательно нужно присвоить имя, иначе на него нельзя будет ссылаться. Поэтому всем кадрам, содержание которых планируется менять, должны быть даны правильные имена. Имена кадров должны начинаться с алфавитно-цифрового символа. Ваши имена не должны начинаться с символа подчеркивания, так как он является первым символом зарезервированных имен кадров, перечисленных в таблице. 
Пример

<p style="text-indent:20px">Создайте файл frames.html в директории public_html и запишите в нем следующие теги: <br><br>

<span class="letter"><.HTML><br>
<.FRAMESET ROWS="*,*"><br>
<.FRAMESET COLS="*,*"><br>
 <.FRAME SRC="frame1.html" NAME="fr1"><br>
 <.FRAME SRC="frame2.html" NAME="fr2"><br>
<./FRAMESET><br>
 <.FRAME SRC="frame3.html" NAME="fr3"><br>
<./FRAMESET><br>
</.HTML> </span>

<p style="text-indent:20px">После этого, создайте файлы frame1.html, frame2.html, frame3.html и заполните их следующим образом: <br> <br>

frame1.html:<br><br>

<span class="letter"><.HTML><.BODY BGCOLOR=white><br>
<.H1>Frame1<./H1><br>
<.A HREF=frame2.html TARGER="fr3">Ссылка на 2 кадр<./A><br>
<./BODY><./HTML> <br><br></span>

frame2.html:<br><br>

<span class="letter"><.HTML><.BODY BGCOLOR=red TEXT=yellow><br>
<.H1>Frame2<./H1><br>
<.A HREF=frame3.html TARGER=_top>Frame3 во все окно</A><br>
<./BODY><./HTML> <br><br></span>

frame3.html:<br><br>

<span class="letter"><.HTML><.BODY BGCOLOR=purple><br>
<.H1>Frame3<./H1><br>
<.A HREF=frame1.html TARGER=_self>Frame1 в это окно<./A><br>
<./BODY><./HTML> <br></span>

