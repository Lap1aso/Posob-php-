                                   <hr/><p align="center"><big>4 Операторы сравнения </big></p><hr/>

<p style="text-indent:20px">Операторы сравнения используются для сопоставления операндов, результатом выражения может быть одно из двух значений — true или false. Операндами могут быть не только числа, но и строки, логические значения и объекты. Однако сравнение может выполняться только для чисел и строк, поэтому операнды, не являющиеся числами или строками, преобразуются.

<p style="text-indent:20px">Если оба операнда не могут быть успешно преобразованы в числа или строки, операторы всегда возвращают false.

<p style="text-indent:20px">Если оба операнда являются строками/числами или могут быть преобразованы в строки/числа, они будут сравниваться как строки/числа.

<p style="text-indent:20px">Если один операнд является строкой/преобразуется в строку, а другой является числом/преобразуется в число, то оператор попытается преобразовать строку в число и выполнить сравнение чисел. Если строка не является числом, она преобразуется в значение NaN и результатом сравнения будет false.

