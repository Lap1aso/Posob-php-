                                         <hr/><p align="center"><big> Выражения в JavaScript </big></p><hr/>


<p style="text-indent:20px">Выражения в JavaScript представляют собой комбинации операндов и операторов.

<p style="text-indent:20px">Операции в выражениях выполняются последовательно в соответствии со значением приоритета (чем больше значение приоритета, тем он выше). Возвращаемый результат не всегда имеет значение того же типа, что и тип обрабатываемых данных. Например, в операциях сравнения участвуют операнды различных типов, но возвращаемый результат всегда будет логического типа.         

<img border="0" src="prim/prim.png" alt="Пример" align="top" width="600" height="200"> 

<p style="text-indent:20px">Операнды — это данные, обрабатываемые сценарием JavaScript. В качестве операндов могут быть как простые типы данных, так и сложные, а также другие выражения.

<p style="text-indent:20px">Операторы — это символы языка, выполняющие различные операции с данными. Операторы могут записываться с помощью символов пунктуации или ключевых слов.

<p style="text-indent:20px">В зависимости от количества операндов различают следующие типы операторов:<br>
унарный — в операции участвует один операнд;<br>
бинарный — в операции участвуют два операнда;<br>
тернарный — комбинирует три операнда.<br>

<p style="text-indent:20px">Простейшая форма выражения — литерал — нечто, вычисляемое само в себя, например, число 100, строка "Hellow world". Переменная тоже может быть выражением, так как она вычисляется в присвоенное ей значение.  

                                           <hr/><p align="center"><big>1 Арифметические операторы </big></p><hr/>

